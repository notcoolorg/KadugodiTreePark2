export function getAvailableMoves(
  currentStation: number | null,
  tickets: Record<string, number>,
  blockedStations: Set<number> = new Set(),
  useBlackCard = false,
): { stationId: number; transports: TransportType[] }[] {
  if (!currentStation || !mapConnections[currentStation]) return [];

  const possible: Record<number, Set<TransportType>> = {};
  const conns = mapConnections[currentStation];

  if (useBlackCard && (tickets.black ?? 0) > 0) {
    const allAdj = new Set<number>([
      ...(conns.taxi ?? []),
      ...(conns.bus ?? []),
      ...(conns.underground ?? []),
      ...(conns.boat ?? []),
    ]);
    for (const s of allAdj) {
      if (!blockedStations.has(s)) {
        if (!possible[s]) possible[s] = new Set();
        possible[s].add("black");
      }
    }
  } else {
    for (const t of ["taxi", "bus", "underground"] as TransportType[]) {
      if ((tickets[t] ?? 0) > 0 && conns[t]) {
        for (const s of conns[t]!) {
          if (!blockedStations.has(s)) {
            if (!possible[s]) possible[s] = new Set();
            possible[s].add(t);
          }
        }
      }
    }

    if ((tickets.black ?? 0) > 0) {
      const allAdj = new Set<number>([
        ...(conns.taxi ?? []),
        ...(conns.bus ?? []),
        ...(conns.underground ?? []),
        ...(conns.boat ?? []),
      ]);
      for (const s of allAdj) {
        if (!blockedStations.has(s)) {
          if (!possible[s]) possible[s] = new Set();
          possible[s].add("black");
        }
      }
    }
  }

  return Object.entries(possible).map(([s, ts]) => ({
    stationId: parseInt(s),
    transports: Array.from(ts),
  }));
}
