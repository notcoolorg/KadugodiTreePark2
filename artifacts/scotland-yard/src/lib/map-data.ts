export const mapConnections: Record<number, Partial<Record<TransportType, number[]>>> = {};
for (let n = 1; n <= 200; n++) {
  if (n === 108) continue;
  mapConnections[n] = {};
  if (_taxi[n]?.length) mapConnections[n].taxi = _taxi[n];
  if (_bus[n]?.length) mapConnections[n].bus = _bus[n];
  if (_ug[n]?.length) mapConnections[n].underground = _ug[n];
  if (_boat[n]?.length) mapConnections[n].boat = _boat[n];
}

