function buildAdj(pairs: [number, number][]): Record<number, number[]> {
  const adj: Record<number, number[]> = {};
  for (let n = 1; n <= 200; n++) {
    if (n !== 108) adj[n] = [];
  }
  for (const [a, b] of pairs) {
    if (adj[a]) adj[a].push(b);
    if (adj[b]) adj[b].push(a);
  }
  return adj;
}

